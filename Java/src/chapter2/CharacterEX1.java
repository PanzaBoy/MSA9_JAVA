package chapter2;

public class CharacterEX1 {
	public static void main(String[] args) {
		char ch1 = 'A'; //문자출력
		System.out.println(ch1);
		System.out.println((int)ch1);
		
		char ch2 = 66;
		System.out.println(ch2);
		
		int ch3 = 67;
		System.out.println(ch3);
		System.out.println((char)ch3);
	}
}